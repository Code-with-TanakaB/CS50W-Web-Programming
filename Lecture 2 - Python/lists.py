# List of names
names = ["Tanaka", "Caroline", "Dessie", "Alice"]

names.append("J.Cole") #adding the string "J.Cole" to the end of the names list using the append method. This method modifies the original list by adding the new element to it.

names.sort() #sorting the names list in place using the sort method. This method modifies the original list by arranging the elements in alphabetical order.

print(names)